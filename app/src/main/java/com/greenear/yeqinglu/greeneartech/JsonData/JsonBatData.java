public class JsonBatData {

    private String id;
    private String bat_id;
    private String soc;
    private String soh;
    private String vol;
    private String res;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBat_id() {
        return bat_id;
    }

    public void setBat_id(String bat_id) {
        this.bat_id = bat_id;
    }

    public String getSoc() {
        return soc;
    }

    public void setSoc(String soc) {
        this.soc = soc;
    }

    public String getSoh() {
        return soh;
    }

    public void setSoh(String soh) {
        this.soh = soh;
    }

    public String getVol() {
        return vol;
    }

    public void setVol(String vol) {
        this.vol = vol;
    }

    public String getRes() {
        return res;
    }

    public void setRes(String res) {
        this.res = res;
    }


}
